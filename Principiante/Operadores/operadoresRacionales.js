//igualdad
let resultado = 10 == 5;
console.log(resultado);

//mayor igual - menor igual
let resultado2 = 10 >= 5;
console.log(resultado2);

//igualdad estricta
let resultado3 = 10 === "10";
console.log(resultado3);

//diferencia
let resultado4 = 10 != 20;
console.log(resultado4);

//diferencia estricta
let resultado5 = 10 != "20";
console.log(resultado5);