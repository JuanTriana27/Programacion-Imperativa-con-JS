let diaSemana = 3;
let mensaje = "";

switch (diaSemana) {
    case 1:
        mensaje = "Lunes";
        break;

    case 2:
        mensaje = "Martes";
        break;

    case 3:
        mensaje = "Miercoles";
        break;

    case 4:
        mensaje = "Jueves";
        break;

    case 5:
        mensaje = "Viernes";
        break;

    case 6:
        mensaje = "Sabado";
        break;

    case 7:
        mensaje = "Domingo";
        break;

    default:
}

console.log(mensaje);